import components.RootComponent
import ui.composables.RootUi

fun main() = application{
    val root = RootComponent(this, DefaultComponentContext(LifecycleRegistry()))
    Tray(
        icon = TrayIcon,
        onAction = {root.isVisible.value=true},
        menu = {
            Item(" Open   ", onClick = {root.isVisible.value = true})
            Item(" Close  ", onClick = {exitApplication()})
        }
    )
    RootUi(root)
}

object TrayIcon : Painter() {
    override val intrinsicSize = Size(256f, 256f)

    override fun DrawScope.onDraw() {
        drawOval(Color(0xFFFFA500))
    }
}